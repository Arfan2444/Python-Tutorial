# indexing  = accessing elements of a sequence using [] (indexing operator)
#             [start : end : step]


creditcardno = "2134-5763-9560-2704"

print(creditcardno[4])

print(creditcardno[0:4])

print(creditcardno[5:])

print(creditcardno[-4:])

print(creditcardno[::3])

print(creditcardno[::-1])